class Assignment 
{
    public static void main(String[] args) {
        int a=4;
        int b=5;
       int result = a+b;
       System.out.println("The sum of a and b is "+result);

       //can do add ,sub, divide , multiply, modulus, increment, decrement, etc.

       // we can add num by itself
       a=a+2;
       System.out.println("The value of a is "+a);
       
       //shortcut 
       b+=4;
       System.out.println("The value of b is "+b);


       //more shortcut 
       a++;   //increment
       a--;   //decrement
        
        
    }
}